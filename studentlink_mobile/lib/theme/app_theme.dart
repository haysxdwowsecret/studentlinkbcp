  static const Color warningLight = Color(0xFFFFC107); // Warning amber
  static const Color backgroundLight = Color(0xFFFFFFFF); // Clean white
  static const Color surfaceLight = Color(0xFFF8F9FA); // Subtle surface
  static const Color textPrimaryLight = Color(0xFF212529); // Primary text
  static const Color textSecondaryLight = Color(0xFF6C757D); // Secondary text
  static const Color borderSubtleLight = Color(0xFFE9ECEF); // Minimal borders

  // Dark theme adaptations
  static const Color primaryDark =
      Color(0xFF4A5BC4); // Lighter institutional blue
  static const Color primaryVariantDark = Color(0xFF1E2A78); // Original primary
  static const Color secondaryDark = Color(0xFF5BA3FF); // Lighter accent blue
  static const Color secondaryVariantDark =
      Color(0xFF2480EA); // Original secondary
